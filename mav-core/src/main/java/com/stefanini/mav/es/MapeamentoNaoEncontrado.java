package com.stefanini.mav.es;

public class MapeamentoNaoEncontrado extends Exception {

	private static final long serialVersionUID = 6555540767215074175L;

	public MapeamentoNaoEncontrado() {
		super();
	}

	public MapeamentoNaoEncontrado(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public MapeamentoNaoEncontrado(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MapeamentoNaoEncontrado(String arg0) {
		super(arg0);
	}

	public MapeamentoNaoEncontrado(Throwable arg0) {
		super(arg0);
	}
	
	

}
